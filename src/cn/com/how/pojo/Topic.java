package cn.com.how.pojo;

import java.util.Date;

/**
 * 论坛的实体类
 * @author admin
 *
 */
public class Topic {

	private Integer id;//id
	private Integer topicId;//主题id
	private Integer fromId;//评论用户的id
	private Integer toId;//评论目标用户的id
	private String type;//帖子的类型
	private String title;//帖子标题
	private String content;//帖子内容
	private Integer isUp;//是否置顶
	private Date date;//发帖或评论日期
	private User user;//一条 帖子/评论 的发表人只有一个
	
	public Integer getIsUp() {
		return isUp;
	}
	public void setIsUp(Integer isUp) {
		this.isUp = isUp;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	
	public Integer getTopicId() {
		return topicId;
	}
	public void setTopicId(Integer topicId) {
		this.topicId = topicId;
	}
	public Integer getFromId() {
		return fromId;
	}
	public void setFromId(Integer fromId) {
		this.fromId = fromId;
	}
	public Integer getToId() {
		return toId;
	}
	public void setToId(Integer toId) {
		this.toId = toId;
	}
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
	
}
